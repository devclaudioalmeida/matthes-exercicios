#Exercício 5.6 Fases da vida
idade = 65
if idade < 2:
    print(f'A pessoa com idade: {idade}, é um nenem!')
elif idade < 4:
    print(f'A pessoa com idade: {idade}, é uma criança!')
elif idade < 13:
    print(f'A pessoa com idade: {idade}, é um garoto(a)!')
elif idade < 20:
    print(f'A pessoa com idade: {idade}, é um adolescente!')
elif idade < 65:
    print(f'A pessoa com idade: {idade}, é um adulto(a)!')
else:
    print(f'A pessoa com idade: {idade}, é um idoso(a)!')